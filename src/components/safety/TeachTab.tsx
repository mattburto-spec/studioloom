function DiscussionPromptsTab({
  prompts,
}: {
  prompts: Array<{
    question: string;
    expected_responses: string[];
    follow_up: string;
  }>;
}) {
  const [expandedId, setExpandedId] = useState<number | null>(0);

  const handleCopyPrompt = (question: string) => {
    navigator.clipboard.writeText(question);
  };

  return (
    <div className="space-y-4">
      <div className="flex items-center justify-between mb-4">
        <h3 className="text-lg font-semibold text-gray-900">Discussion Prompts</h3>
        <p className="text-sm text-gray-600">{prompts.length} prompts</p>
      </div>

      {prompts.map((prompt, idx) => (
        <div key={idx} className="border border-gray-200 rounded-lg overflow-hidden bg-white">
          <button
            onClick={() => setExpandedId(expandedId === idx ? null : idx)}
            className="w-full px-5 py-4 flex items-start gap-3 hover:bg-gray-50 transition-colors text-left"
          >
            <ChevronDownIcon />
            <div className="flex-grow">
              <p className="font-semibold text-gray-900 text-base leading-snug">
                {prompt.question}
              </p>
            </div>
            <button
              onClick={(e) => {
                e.stopPropagation();
                handleCopyPrompt(prompt.question);
              }}
              className="flex-shrink-0 p-2 text-gray-500 hover:text-purple-600 transition-colors"
              title="Copy to clipboard"
            >
              <CopyIcon />
            </button>
          </button>

          {expandedId === idx && (
            <div className="border-t border-gray-200 px-5 py-4 bg-gray-50 space-y-4">
              <div>
                <p className="text-xs font-semibold text-gray-700 mb-2 uppercase">
                  Expected Student Responses
                </p>
                <ul className="space-y-2">
                  {prompt.expected_responses.map((response, rIdx) => (
                    <li key={rIdx} className="flex items-start gap-2 text-sm text-gray-700">
                      <span className="text-purple-500 font-bold mt-0.5">•</span>
                      {response}
                    </li>
                  ))}
                </ul>
              </div>

              <div className="bg-blue-50 border-l-4 border-blue-500 p-3 rounded">
                <p className="text-xs font-semibold text-blue-900 mb-1">FOLLOW-UP QUESTION</p>
                <p className="text-sm text-blue-800">{prompt.follow_up}</p>
              </div>
            </div>
          )}
        </div>
      ))}
    </div>
  );
}

