export default function TeachTab({ badge, teacherGuide }: TeachTabProps) {
  const [activeTab, setActiveTab] = useState<TeachSubTab>("lesson-plan");

  // Prepare data
  const lessonScript = teacherGuide?.demo_script || autoGenerateLessonPlan(badge);
  const discussionPrompts = teacherGuide?.discussion_prompts || autoGenerateDiscussionPrompts(badge);
  const stationCards = teacherGuide?.station_cards || autoGenerateStationCards(badge);
  const checklist = teacherGuide?.practical_checklist || autoGenerateChecklist(badge);

  const tabs: Array<{ id: TeachSubTab; label: string }> = [
    { id: "lesson-plan", label: "Lesson Plan" },
    { id: "projector", label: "Projector View" },
    { id: "discussion", label: "Discussion Prompts" },
    { id: "stations", label: "Station Cards" },
    { id: "checklist", label: "Checklist" },
  ];

  return (
    <div className="space-y-6">
      {/* Sub-tabs */}
      <div className="flex gap-2 border-b border-gray-200 overflow-x-auto">
        {tabs.map((tab) => (
          <button
            key={tab.id}
            onClick={() => setActiveTab(tab.id)}
            className={`px-4 py-3 font-medium text-sm whitespace-nowrap border-b-2 transition-colors ${
              activeTab === tab.id
                ? "border-purple-600 text-purple-600"
                : "border-transparent text-gray-600 hover:text-gray-900"
            }`}
          >
            {tab.label}
          </button>
        ))}
      </div>

      {/* Tab content */}
      <div>
        {activeTab === "lesson-plan" && <LessonPlanTab badge={badge} script={lessonScript} />}
        {activeTab === "projector" && <ProjectorViewTab badge={badge} />}
        {activeTab === "discussion" && <DiscussionPromptsTab prompts={discussionPrompts} />}
        {activeTab === "stations" && (
          <StationCardsTab cards={stationCards} badgeName={badge.name} />
        )}
        {activeTab === "checklist" && (
          <ChecklistTab checklist={checklist} badgeName={badge.name} />
        )}
      </div>
    </div>
  );
}
