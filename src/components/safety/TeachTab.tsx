/**
 * Calculate estimated lesson duration in minutes
 * 5 min intro + 3 min per learn card/block + 10 min for Spot the Hazard + 5 min per discussion + 5 min wrap-up
 */
function calculateLessonDuration(
  blocks: ContentBlock[],
  discussionCount: number = 3
): number {
  let minutes = 5; // intro
  minutes += blocks.length * 3; // 3 min per block

  // Check if there's a Spot the Hazard block
  const hasHazardBlock = blocks.some((b) => b.type === "spot_the_hazard");
  if (hasHazardBlock) minutes += 10;

  minutes += discussionCount * 5; // discussions
  minutes += 5; // wrap-up

  return minutes;
}

/**
 * Auto-generate discussion prompts from badge content
 */
function autoGenerateDiscussionPrompts(badge: TeachTabProps["badge"]) {
  const blocks = getBlocksFromBadge(badge);
  const prompts = [
    {
      question: `Why do you think ${badge.name.toLowerCase()} is important in our workshop?`,
      expected_responses: [
        "It keeps us safe",
        "It prevents accidents",
        "It protects our health",
      ],
      follow_up: "Can you give a real example of what could happen if we didn't follow these rules?",
    },
    {
      question: "What are the most critical rules you need to remember?",
      expected_responses: blocks
        .filter((b): b is KeyConceptBlock => b.type === "key_concept")
        .slice(0, 3)
        .map((b) => b.title),
      follow_up: "How will you remember these rules when you're working?",
    },
    {
      question: "Have you seen anyone break one of these rules? What happened?",
      expected_responses: [
        "They got hurt",
        "The teacher told them off",
        "They damaged equipment",
      ],
      follow_up: "What should they have done instead?",
    },
    {
      question: "If you see a friend breaking a safety rule, what should you do?",
      expected_responses: [
        "Tell them to stop",
        "Tell the teacher",
        "Help them do it safely",
      ],
      follow_up: "How can you say it in a way that doesn't embarrass them?",
    },
    {
      question: "What would you add to these rules to make the workshop even safer?",
      expected_responses: [
        "Better signage",
        "More first aid kits",
        "A safety buddy system",
      ],
      follow_up: "Why do you think that would help?",
    },
  ];

  return prompts;
}

/**
 * Auto-generate station cards
 */
function autoGenerateStationCards(badge: TeachTabProps["badge"]) {
  const blocks = getBlocksFromBadge(badge);
  const keyConceptBlocks = blocks.filter(
    (b): b is KeyConceptBlock => b.type === "key_concept"
  );

  return keyConceptBlocks.map((block, idx) => ({
    station_name: `Station ${idx + 1}: ${block.title}`,
    rules: [
      block.content.split("\n")[0] || block.title,
      ...(block.examples?.slice(0, 2) || []),
    ],
    qr_code_url: undefined,
    illustration: undefined,
  }));
}

/**
 * Auto-generate checklist
 */
function autoGenerateChecklist(badge: TeachTabProps["badge"]) {
  const blocks = getBlocksFromBadge(badge);
  const keyConceptBlocks = blocks.filter(
    (b): b is KeyConceptBlock => b.type === "key_concept"
  );

  return keyConceptBlocks.map((block) => ({
    criterion: block.title,
    observable_action: block.content.split("\n")[0] || `Student understands ${block.title}`,
  }));
}

/**
 * Auto-generate lesson plan script
 */
function autoGenerateLessonPlan(badge: TeachTabProps["badge"]) {
  const blocks = getBlocksFromBadge(badge);
  const script: Array<{
    step: number;
    action: string;
    say: string;
    show: string;
    timing_seconds: number;
  }> = [];

  let step = 1;

  // Opening
  script.push({
    step,
    action: "Welcome & hook",
    say: `Today we're learning about ${badge.name}. This is important because it keeps you and everyone else safe in our workshop.`,
    show: `Badge name and description on projector`,
    timing_seconds: 300,
  });
  step++;

  // Key concepts
  const keyConceptBlocks = blocks.filter(
    (b): b is KeyConceptBlock => b.type === "key_concept"
  );
  keyConceptBlocks.forEach((block) => {
    script.push({
      step,
      action: `Present: ${block.title}`,
      say: block.content,
      show: block.title,
      timing_seconds: 180,
    });
    step++;
  });

  // Spot the Hazard activity
  const hazardBlock = blocks.find(
    (b): b is SpotTheHazardBlock => b.type === "spot_the_hazard"
  );
  if (hazardBlock) {
    script.push({
      step,
      action: "Interactive: Spot the Hazard",
      say: `Now let's test what you've learned. I'm going to show you a workshop scene. Can you spot all the safety hazards? Think about what could go wrong and how we'd fix it.`,
      show: `Workshop scene - students spot hazards`,
      timing_seconds: 600,
    });
    step++;
  }

  // Discussion
  script.push({
    step,
    action: "Group discussion",
    say: `Why do you think these rules matter? Has anyone seen something unsafe before?`,
    show: `Discussion prompts on projector`,
    timing_seconds: 300,
  });
  step++;

  // Wrap-up
  script.push({
    step,
    action: "Conclusion",
    say: `Remember: safety is everyone's responsibility. If you see something unsafe, speak up. Let's make our workshop the safest place in school.`,
    show: `Key rules summary`,
    timing_seconds: 300,
  });

  return script;
}

