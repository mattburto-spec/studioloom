/**
 * Auto-generate station cards from ALL block types
 */
function autoGenerateStationCards(badge: TeachTabProps["badge"]) {
  const blocks = getBlocksFromBadge(badge);

  return blocks.map((block, idx) => {
    let title = "";
    let rules: string[] = [];
    let activity = "";
    let icon = "shield";
    let warning = "";
    let color = "purple";

    if (block.type === "key_concept") {
      const b = block as KeyConceptBlock;
      title = b.title;
      icon = b.icon || "lightbulb";
      rules = b.tips?.slice(0, 4) || [];
      if (rules.length === 0 && b.content) {
        rules = b.content.split(/[.\n]/).filter(s => s.trim().length > 10).slice(0, 4);
      }
      if (b.examples?.length) activity = `Discussion: "${b.examples[0]}"`;
      if (b.warning) warning = b.warning;
      color = "purple";
    } else if (block.type === "spot_the_hazard") {
      const b = block as import("@/lib/safety/content-blocks").SpotTheHazardBlock;
      title = b.title || "Spot the Hazard";
      icon = "eye";
      rules = b.hazards.slice(0, 4).map(h => `${h.severity === "critical" ? "⚠️" : "⚡"} ${h.label}: ${h.explanation}`);
      activity = `Find all ${b.total_hazards} hazards in the workshop scene`;
      color = "red";
    } else if (block.type === "scenario") {
      const b = block as import("@/lib/safety/content-blocks").ScenarioBlock;
      title = b.title;
      icon = "users";
      rules = [b.setup];
      const correct = b.branches.find(br => br.is_correct);
      if (correct) rules.push(`Best response: ${correct.choice_text}`);
      activity = "Discuss: What would YOU do in this situation?";
      color = "blue";
    } else if (block.type === "before_after") {
      const b = block as import("@/lib/safety/content-blocks").BeforeAfterBlock;
      title = b.title;
      icon = "refresh";
      rules = [
        ...b.before.hazards.slice(0, 2).map(h => `❌ ${h}`),
        ...b.after.principles.slice(0, 2).map(p => `✅ ${p}`),
      ];
      activity = `Key difference: ${b.key_difference}`;
      color = "amber";
    } else if (block.type === "comprehension_check") {
      const b = block as import("@/lib/safety/content-blocks").ComprehensionCheckBlock;
      title = "Quick Check";
      icon = "check";
      rules = [b.question, ...b.options.map((o, i) => `${String.fromCharCode(65 + i)}. ${o}`)];
      activity = `Answer: ${String.fromCharCode(65 + b.correct_index)}`;
      color = "green";
    } else {
      title = (block as any).title || `Activity ${idx + 1}`;
      rules = [(block as any).content || (block as any).narrative || "Complete this station activity"];
      color = "purple";
    }

    return {
      station_name: `Station ${idx + 1}: ${title}`,
      rules,
      activity,
      icon,
      warning,
      color,
      qr_code_url: undefined,
    };
  });
}

/**
 * Auto-generate checklist
 */
function autoGenerateChecklist(badge: TeachTabProps["badge"]) {
  const blocks = getBlocksFromBadge(badge);
  const keyConceptBlocks = blocks.filter(
    (b): b is KeyConceptBlock => b.type === "key_concept"
  );

  return keyConceptBlocks.map((block) => ({
    criterion: block.title,
    observable_action: block.content.split("\n")[0] || `Student understands ${block.title}`,
  }));
}

/**
 * Auto-generate lesson plan script
 */
function autoGenerateLessonPlan(badge: TeachTabProps["badge"]) {
  const blocks = getBlocksFromBadge(badge);
  const script: Array<{
    step: number;
    action: string;
    say: string;
    show: string;
    timing_seconds: number;
  }> = [];

  let step = 1;

  // Opening
  script.push({
    step,
    action: "Welcome & hook",
    say: `Today we're learning about ${badge.name}. This is important because it keeps you and everyone else safe in our workshop.`,
    show: `Badge name and description on projector`,
    timing_seconds: 300,
  });
  step++;

  // Key concepts
  const keyConceptBlocks = blocks.filter(
    (b): b is KeyConceptBlock => b.type === "key_concept"
  );
  keyConceptBlocks.forEach((block) => {
    script.push({
      step,
      action: `Present: ${block.title}`,
      say: block.content,
      show: block.title,
      timing_seconds: 180,
    });
    step++;
  });

  // Spot the Hazard activity
  const hazardBlock = blocks.find(
    (b): b is SpotTheHazardBlock => b.type === "spot_the_hazard"
  );
  if (hazardBlock) {
    script.push({
      step,
      action: "Interactive: Spot the Hazard",
      say: `Now let's test what you've learned. I'm going to show you a workshop scene. Can you spot all the safety hazards? Think about what could go wrong and how we'd fix it.`,
      show: `Workshop scene - students spot hazards`,
      timing_seconds: 600,
    });
    step++;
  }

  // Discussion
  script.push({
    step,
    action: "Group discussion",
    say: `Why do you think these rules matter? Has anyone seen something unsafe before?`,
    show: `Discussion prompts on projector`,
    timing_seconds: 300,
  });
  step++;

  // Wrap-up
  script.push({
    step,
    action: "Conclusion",
    say: `Remember: safety is everyone's responsibility. If you see something unsafe, speak up. Let's make our workshop the safest place in school.`,
    show: `Key rules summary`,
    timing_seconds: 300,
  });

  return script;
}

